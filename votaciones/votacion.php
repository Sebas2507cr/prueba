function enviarVoto() {
    const seleccionada = document.querySelector('.casilla-voto[style*="color: black"]');
    if (!seleccionada) {
        alert("Por favor, selecciona una opción antes de enviar tu voto.");
        return;
    }
    
    const boton = document.querySelector('.boton-enviar');
    boton.innerHTML = '<i class="fas fa-check"></i> Voto enviado';
    boton.disabled = true;
    boton.style.background = "#4CAF50";
    
    // Deshabilitar todas las casillas
    document.querySelectorAll('.casilla-voto').forEach(c => {
        c.onclick = null;
        c.style.cursor = "default";
    });
}
